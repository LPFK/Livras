export class Livre {

    Titre:string;
    Auteur:string;
    Date:string;
    Type:string;

    constructor(pTitre:string, pAuteur:string, pDate:string, pType:string){
        
        this.Titre = pTitre;
        this.Auteur = pAuteur;
        this.Date = pDate;
        this.Type = pType;
    }
}
